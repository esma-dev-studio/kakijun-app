// 語の "X/よみ" → "X" にクリーニング
function cleanWord(w) {
  const i = w.indexOf('/');
  return i < 0 ? w : w.slice(0, i);
}

const K2ORDER = '引羽雲園遠何科夏家歌画回会海絵外角楽活間丸岩顔汽記帰弓牛魚京強教近兄形計元言原戸古午後語工公広交光考行高黄合谷国黒今才細作算止市矢姉思紙寺自時室社弱首秋週春書少場色食心新親図数西声星晴切雪船線前組走多太体台地池知茶昼長鳥朝直通弟店点電刀冬当東答頭同道読内南肉馬売買麦半番父風分聞米歩母方北毎妹万明鳴毛門夜野友用曜来里理話';

const byChar = {};
for (const batch of RAW) for (const e of batch) byChar[e.k] = e;

const kanaRe = /^[ぁ-んァ-ヶー]+$/;
const yomiRe = /^[ぁ-ん。、 ？]+$/;
const issues = [];
const entries = [];

for (const k of K2ORDER) {
  let e = byChar[k];
  if (!e) { issues.push(k + ':missing'); continue; }
  if (FIXES[k]) e = Object.assign({}, e, FIXES[k]);
  const words = e.words.map(([w, r]) => [cleanWord(w), r]);
  const rd = READINGS[k] || { on: [], kun: [] };
  const on = rd.on.slice(0, 2);
  const kun = rd.kun.slice(0, 2);
  // 検証
  if (!e.mean) issues.push(k + ':nomean');
  for (const [w, r] of words) {
    if (!w.includes(k)) issues.push(k + ':word-no-kanji:' + w);
    if (!kanaRe.test(r)) issues.push(k + ':word-rdg:' + r);
  }
  if (!e.ex[0].includes(k)) issues.push(k + ':ex-no-kanji:' + e.ex[0]);
  if (!yomiRe.test(e.ex[1])) issues.push(k + ':yomi:' + e.ex[1]);
  entries.push({ k, on, kun, mean: e.mean, words, ex: e.ex });
}

if (issues.length) {
  console.log('ISSUES (' + issues.length + '):');
  for (const i of issues) console.log('  ' + i);
  process.exit(1);
}

// data/kanjiinfo.js に追記
const lines = entries.map(e => {
  const j = (x) => JSON.stringify(x, null, 0);
  return `"${e.k}":{on:${j(e.on)},kun:${j(e.kun)},mean:${j(e.mean)},words:${j(e.words)},ex:${j(e.ex)}}`;
});
const block = '\n// ―― 2ねんせい ――\n' + lines.join(',\n') + '\n';

